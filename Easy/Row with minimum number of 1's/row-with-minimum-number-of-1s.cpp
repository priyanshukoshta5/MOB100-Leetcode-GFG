class Solution {
  public:
    int minRow(int n, int m, vector<vector<int>> a) {
        int min1 = m, ansRow = 1;
        for(int r = 0; r < n; r++)
        {
            int one = count(a[r].begin(), a[r].end(), 1);
            if(one < min1)
            {
                min1 = one;
                ansRow = r + 1;
            }
        }
        return ansRow;
    }
};

