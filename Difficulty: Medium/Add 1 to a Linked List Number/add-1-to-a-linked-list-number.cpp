class Solution {
  public: 
    int addIt(Node *head){
        if(head == nullptr)
            return 1;
        
        int rem = addIt(head->next);
        head->data += rem;
        rem = head->data / 10;
        head->data %= 10;
        return rem;
    }
    
  public:
    Node* addOne(Node* head) {
        int carry = addIt(head);
        if(carry == 1)
        {
            Node *newHead = new Node(carry);
            newHead->next = head;
            head = newHead;
        }
        return head;
    }
};

