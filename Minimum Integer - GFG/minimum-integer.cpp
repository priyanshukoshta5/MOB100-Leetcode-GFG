class Solution {
  public:
    int minimumInteger(int N, vector<int> &A) {
        long long int sum = 0;
        for(int i: A)
            sum += i;
            
        int x = -1;
        for(int i: A)
        {
            long long val = (long long int)N*(long long int)i;
            if(sum <= val)
            {
                if(x == -1) x = i;
                else x = min(x, i);
            }
        }
        
        return x;
    }
};


